package com.notification.inotythemehhhhh.objects;

public class EventEntity {
    private String mNameOfEvent;
    private long mStartEvent;
    private long mEndEvent;
    private String mDescriptions;

    public String getNameOfEvent() {
        return mNameOfEvent;
    }

    public void setNameOfEvent(String nameOfEvent) {
        mNameOfEvent = nameOfEvent;
    }

    public long getStartEvent() {
        return mStartEvent;
    }

    public void setStartEvent(long startEvent) {
        mStartEvent = startEvent;
    }

    public long getEndEvent() {
        return mEndEvent;
    }

    public void setEndEvent(long endEvent) {
        mEndEvent = endEvent;
    }

    public String getDescriptions() {
        return mDescriptions;
    }

    public void setDescriptions(String descriptions) {
        mDescriptions = descriptions;
    }
}
